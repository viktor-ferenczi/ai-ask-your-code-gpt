#!/bin/sh

NAME="downloader-$INSTANCE_INDEX"
TITLE="Downloader-$INSTANCE_INDEX"

INSTANCE_COUNT=4

WORKING_DIR="$HOME/src/downloader"
COMMAND_LINE="python -O -u downloader.py -$INSTANCE_INDEX-"

LOG_PATH_BASENAME="$HOME/log/$NAME-$INSTANCE_INDEX"

if [ "$ENVIRONMENT" == "PRODUCTION" ]; then
  export HTTP_PORT=$((41000 + INSTANCE_INDEX))
else
  export HTTP_PORT=$((41500 + INSTANCE_INDEX))
fi

CANARY="http"
CANARY_URL="http://127.0.0.1:$HTTP_PORT"
CANARY_TIMEOUT=10
